 Email=@Email,
 Interno=@Interno,
 Administrador=@Administrador,
 Iniciales=@Iniciales,
 Password=@Password,
 SisMan=@SisMan,
 HorasJornada=@HorasJornada,
 IdSector1=@IdSector1,
 IdCargo1=@IdCargo1,
 IdSector2=@IdSector2,
 IdCargo2=@IdCargo2,
 IdSector3=@IdSector3,
 IdCargo3=@IdCargo3,
 IdSector4=@IdSector4,
 IdCargo4=@IdCargo4,
 Dominio=@Dominio,
 SisMat=@SisMat,
 FechaNacimiento=@FechaNacimiento,
 TipoUsuario=@TipoUsuario,
 GrupoDeCarga=@GrupoDeCarga,
 CuentaBancaria=@CuentaBancaria,
 InformacionAuxiliar=@InformacionAuxiliar,
